HRESULT DBVARIANT::to_dbtimestamp(value_dbstamp_n_type* const value) const
{
 assert(value);

 const TDBVariant_TypeCvtContext_Std ctx;

 return DBVariant__to_DBTIMESTAMP_ex(&ctx,*this,value);
}//to_dbtimestamp

