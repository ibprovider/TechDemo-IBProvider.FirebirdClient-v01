HRESULT DBVARIANT::to_filetime(value_filetime_n_type* const value)const
{
 assert(value);

 const TDBVariant_TypeCvtContext_Std ctx;

 return DBVariant__to_FILETIME_ex(&ctx,*this,value);
}//to_filetime

