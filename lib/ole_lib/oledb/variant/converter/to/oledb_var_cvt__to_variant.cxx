HRESULT DBVARIANT::to_variant(VARIANT* const value) const
{
 assert(value);

 const TDBVariant_TypeCvtContext_Std ctx;

 return DBVariant__to_VARIANT_ex(&ctx,*this,value);
}//to_variant

